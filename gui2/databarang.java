public void Tampildata(){
    Object [] baris = {"idobat","nama_obat","harga","jumlah_stok","satuan"};
    model = new DefaultTableModel(null, baris);
    Tabel.setModel(model);
    Connection connection = new koneksi().getConnection();
    try{
        String sql = "select * from tbdataobat order by idobat";
        java.sql.Statement statement = connection.createStatement();
        java.sql.ResultSet hasil = statement.executeQuery(sql);
        while (hasil.next()){
            String id = hasil.getString("idobat");
            String nama = hasil.getString("nama_obat");
            String sto= hasil.getString("jumlah_stok");
            String hrg = hasil.getString("harga");
            String st = hasil.getString("satuan");
            String [] data = {id, nama, sto, hrg, st};
            model.addRow(data);
        }
    } catch (Exception e){
       //tJOptionPane.showMessageDialog(null, "Menampilkan data gagal","informasi", JOptionPane.INFORMATION_MESSAGE);
        JOptionPane.showMessageDialog(null,"Menampilkan data gagal" +e);
    }
}    


