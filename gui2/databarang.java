    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
      if("".equals(idbarang.getText())||"".equals(txtnama.getText())||"".equals(txtstock.getText())||"".equals(txthrg.getText())||"".equals(cmbsatuan.getSelectedItem())){
            JOptionPane.showMessageDialog(null, "Lengkapi data!");
        }else{
             try {
            st=cn.createStatement();
            st.executeUpdate("insert into tbdataobat set " + "idobat = '" + idbarang.getText() + "', nama_obat='" + txtnama.getText() + "', jumlah_stok = '" + txthrg.getText() + "', harga = '" + txtstock.getText() + "', satuan='" + cmbsatuan.getSelectedItem()+ "';");
                 Tampildata();
                
            
            JOptionPane.showMessageDialog(null, "Berhasil");
        } catch (Exception e) {
            e.printStackTrace();
        }
        }
             bersih();
    }//GEN-LAST:event_simpanActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
    if("".equals(idbarang.getText())||"".equals(txtnama.getText())||"".equals(txthrg.getText())||"".equals(txtstock.getText())||"".equals(cmbsatuan.getSelectedItem())){
            JOptionPane.showMessageDialog(null, "Isi semua Kolom");
        }else{
            try {
                st=cn.createStatement();
                 st.executeUpdate("update tbdataobat set "+"nama_obat='"+txtnama.getText()+"',"+"jumlah_stok='"+txthrg.getText()+"',"+"harga='"+txtstock.getText()+"',"+"satuan='"+cmbsatuan.getSelectedItem()+"' where idobat='"+model.getValueAt(Tabel.getSelectedRow(),0)+"';");
                Tampildata();
                JOptionPane.showMessageDialog(null, "Berhasil");
            } catch (Exception e) {
               JOptionPane.showMessageDialog(null, "Gagal diupdate! "+e.getMessage());
            }
         }
        bersih();
    }//GEN-LAST:event_editActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
if("".equals(idbarang.getText())||"".equals(txtnama.getText())||"".equals(txtstock.getText())||"".equals(txthrg.getText())||"".equals(cmbsatuan.getSelectedItem())){
            JOptionPane.showMessageDialog(null, "Pilih Terlebih dahulu");
        }else{
            try {
                int jawab;
                if ((jawab=JOptionPane.showConfirmDialog(null,"Hapus ?","konfirmasi",JOptionPane.YES_NO_OPTION))==0) {
                    st.executeUpdate("delete from tbdataobat where "+"idobat='"+model.getValueAt(Tabel.getSelectedRow(),0)+"'");
                    Tampildata();
                    JOptionPane.showMessageDialog(null, "Berhasil dihapus");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal diHapus! "+e.getMessage());
            }
         }   
bersih();
    }//GEN-LAST:event_hapusActionPerformed

